PEMBAHASAN

A. Urgensi Pendidikan Multikultural dalam Konteks HAM di Indonesia
Implementasi HAM di Indonesia seringkali terbentur pada ego sektoral dan fanatisme kelompok. Pendidikan multikultural hadir sebagai solusi preventif. Dalam bingkai multikultural, siswa diajarkan bahwa "berbeda itu biasa" dan "berbeda itu hak". Ketika seorang siswa memahami bahwa temannya yang berbeda agama atau suku memiliki hak yang sama untuk beribadah dan berekspresi, maka di situlah benih implementasi HAM mulai tumbuh.

Sekolah tidak boleh menjadi menara gading yang terpisah dari realitas sosial. Kurikulum harus didesain untuk menyentuh aspek afektif siswa. Misalnya, dalam pelajaran Sejarah atau PPKn, tidak hanya menghafal pasal-pasal UUD 1945, tetapi mendiskusikan kasus-kasus pelanggaran HAM yang terjadi akibat ketidakmampuan masyarakat menerima perbedaan.

B. Strategi Implementasi dalam Pembelajaran
1. Pendekatan Kontribusi (The Contribution Approach): Mengintegrasikan materi tentang pahlawan, hari libur, dan elemen budaya dari berbagai kelompok etnis ke dalam kurikulum.
2. Pendekatan Aditif (The Additive Approach): Menambahkan konten, konsep, tema, dan perspektif tanpa mengubah struktur kurikulum dasarnya.
3. Pendekatan Transformasi (The Transformation Approach): Mengubah struktur kurikulum agar siswa dapat melihat konsep, isu, peristiwa, dan tema dari perspektif berbagai kelompok etnis dan budaya.
4. Pendekatan Aksi Sosial (The Social Action Approach): Siswa membuat keputusan tentang isu-isu sosial penting dan mengambil tindakan untuk membantu memecahkannya. Ini adalah level tertinggi implementasi HAM, di mana siswa tidak hanya tahu teori HAM, tapi berani bertindak membela hak orang lain.

